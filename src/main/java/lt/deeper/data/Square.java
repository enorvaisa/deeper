package lt.deeper.data;

/**
 * Object for storing square information
 * 
 * @author Evaldas Norvai≈°a
 *
 */
public class Square {
    
    private double sp1x;
    private double sp1y;
    private double sp2x;
    private double sp2y;
    private double sp3x;
    private double sp3y;
    private double sp4x;
    private double sp4y;
    
    public Square(double sp1x, double sp1y, double sp2x, double sp2y, double sp3x, double sp3y, double sp4x, double sp4y) {
        this.sp1x = sp1x;
        this.sp1y = sp1y;
        this.sp2x = sp2x;
        this.sp2y = sp2y;
        this.sp3x = sp3x;
        this.sp3y = sp3y;
        this.sp4x = sp4x;
        this.sp4y = sp4y;
    }

    public double getSp1x() {
        return sp1x;
    }

    public void setSp1x(double sp1x) {
        this.sp1x = sp1x;
    }

    public double getSp1y() {
        return sp1y;
    }

    public void setSp1y(double sp1y) {
        this.sp1y = sp1y;
    }

    public double getSp2x() {
        return sp2x;
    }

    public void setSp2x(double sp2x) {
        this.sp2x = sp2x;
    }

    public double getSp2y() {
        return sp2y;
    }

    public void setSp2y(double sp2y) {
        this.sp2y = sp2y;
    }

    public double getSp3x() {
        return sp3x;
    }

    public void setSp3x(double sp3x) {
        this.sp3x = sp3x;
    }

    public double getSp3y() {
        return sp3y;
    }

    public void setSp3y(double sp3y) {
        this.sp3y = sp3y;
    }

    public double getSp4x() {
        return sp4x;
    }

    public void setSp4x(double sp4x) {
        this.sp4x = sp4x;
    }

    public double getSp4y() {
        return sp4y;
    }

    public void setSp4y(double sp4y) {
        this.sp4y = sp4y;
    }
    
}
